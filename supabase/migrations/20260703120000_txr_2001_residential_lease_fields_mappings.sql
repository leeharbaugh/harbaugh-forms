-- ---------------------------------------------------------------------------
-- Ensure TXR-2001 form template exists (idempotent)
-- ---------------------------------------------------------------------------

insert into public.forms (
  form_code,
  form_name,
  form_category,
  state_code,
  version_label,
  source_storage_path,
  description,
  status
)
select
  'TXR-2001',
  'TXR-2001 Residential Lease',
  'CONTRACT',
  'TX',
  '01-05-26',
  'TXR-2001/ResidentialLease.pdf',
  'Texas REALTORS residential lease agreement (flat PDF, no AcroForm fields).',
  'ACTIVE'
where not exists (
  select 1
  from public.forms existing
  where existing.status = 'ACTIVE'
    and (
      lower(trim(existing.form_code)) in ('txr-2001', 'txr_2001')
      or lower(trim(existing.form_code)) like '%txr-2001%'
      or lower(trim(existing.form_code)) like '%txr_2001%'
    )
);

-- ---------------------------------------------------------------------------
-- Resolve TXR-2001 form
-- ---------------------------------------------------------------------------

create temp table txr_2001_form on commit drop as
select f.id as form_id
from public.forms f
where f.status = 'ACTIVE'
  and (
    lower(trim(f.form_code)) in ('txr-2001', 'txr_2001')
    or lower(trim(f.form_code)) like '%txr-2001%'
    or lower(trim(f.form_code)) like '%txr_2001%'
    or (
      lower(f.form_name) like '%residential%'
      and lower(f.form_name) like '%lease%'
      and lower(f.form_name) not like '%listing%'
      and lower(f.form_name) not like '%application%'
      and lower(f.form_name) not like '%guaranty%'
    )
  )
order by
  case when lower(trim(f.form_code)) in ('txr-2001', 'txr_2001') then 0 else 1 end,
  case
    when lower(f.form_name) like '%txr-2001%' or lower(f.form_name) like '%2001%' then 0
    else 1
  end,
  f.id
limit 1;

